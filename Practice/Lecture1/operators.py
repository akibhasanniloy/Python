# Arithmetic operator
a = 5
b = 2

print(a + b)
print(a - b)
print(a / b)
print(a * b)
print(a % b) #reminder
print(a ** b) #power a^b

# Relational Operator
print(a == b)
print(a != b)
print(a >= b)
print(a <= b)

# Logical Operator
print(not True)
print(not False)
print(not (a>b))

val1 = True
